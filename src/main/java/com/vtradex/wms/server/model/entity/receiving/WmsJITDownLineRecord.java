package com.vtradex.wms.server.model.entity.receiving;

import com.vtradex.thorn.server.model.VersionalEntity;
/**
 * 记录JIT下线料的收货信息
 * @author Administrator
 *
 */
public class WmsJITDownLineRecord extends VersionalEntity{
	
	/**
	 * ASN明细
	 */
	private WmsASNDetail detail;
	/**
	 * ASN编码
	 */
	private String asnCode;
	/**
	 * 回传数量
	 */
	private Double backQuantity = 0D;
	public WmsASNDetail getDetail() {
		return detail;
	}
	public void setDetail(WmsASNDetail detail) {
		this.detail = detail;
	}
	public String getAsnCode() {
		return asnCode;
	}
	public void setAsnCode(String asnCode) {
		this.asnCode = asnCode;
	}
	public Double getBackQuantity() {
		return backQuantity;
	}
	public void setBackQuantity(Double backQuantity) {
		this.backQuantity = backQuantity;
	}
	
	
}
