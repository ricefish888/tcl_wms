package com.vtradex.wms.webservice.sap.model;

import com.vtradex.wms.server.service.annotation.XmlDataNote;
 
/***
 * SAP接口  物料主数据  数组
 * @author administrator
 *
 */
@XmlDataNote("SapItemArray")
public class SapItemArray {
	
	private SapItem[] sapItems;
	
	private String ROWCNT;
	
	private String MESSAGEID;
	
	private String TYPE; // 	CHAR(02)	操作类型	必输	I为新增 U为更改 D为删除

	public SapItem[] getSapItems() {
		return sapItems;
	}

	public void setSapItems(SapItem[] sapItems) {
		this.sapItems = sapItems;
	}

	public String getROWCNT() {
		return ROWCNT;
	}

	public void setROWCNT(String rOWCNT) {
		ROWCNT = rOWCNT;
	}

	public String getMESSAGEID() {
		return MESSAGEID;
	}

	public void setMESSAGEID(String mESSAGEID) {
		MESSAGEID = mESSAGEID;
	}

	public String getTYPE() {
		return TYPE;
	}

	public void setTYPE(String tYPE) {
		TYPE = tYPE;
	}

}
