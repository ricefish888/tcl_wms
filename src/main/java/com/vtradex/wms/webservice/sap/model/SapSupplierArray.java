package com.vtradex.wms.webservice.sap.model;

import com.vtradex.wms.server.service.annotation.XmlDataNote;
 
/***
 * SAP接口  供应商主数据  数组
 * @author administrator
 *
 */
@XmlDataNote("SapSupplierArray")
public class SapSupplierArray {
	
	private SapSupplier[] sapSuppliers;
	
	private String ROWCNT;
	
	private String MESSAGEID;
	
	private String TYPE; //	CHAR(02)	I为新增U为修改

	public SapSupplier[] getSapSuppliers() {
		return sapSuppliers;
	}

	public void setSapSuppliers(SapSupplier[] sapSuppliers) {
		this.sapSuppliers = sapSuppliers;
	}

	public String getROWCNT() {
		return ROWCNT;
	}

	public void setROWCNT(String rOWCNT) {
		ROWCNT = rOWCNT;
	}

	public String getMESSAGEID() {
		return MESSAGEID;
	}

	public void setMESSAGEID(String mESSAGEID) {
		MESSAGEID = mESSAGEID;
	}

	public String getTYPE() {
		return TYPE;
	}

	public void setTYPE(String tYPE) {
		TYPE = tYPE;
	}
	
}
