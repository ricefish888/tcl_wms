SELECT
 LOC.ID AS "序号",
 LOC.CODE AS "代码",
 SUM(INV.QTY)  AS "数量"
 FROM WMS_LOCATION LOC
 LEFT JOIN WMS_WAREHOUSE WAREHOUSE ON WAREHOUSE.ID = LOC.WAREHOUSE_ID 
 LEFT JOIN WMS_INVENTORY INV ON INV.LOCATION_ID = LOC.ID 
WHERE 1=1
 AND LOC.STATUS = 'ENABLED'
 AND LOC.TYPE = 'SHIP'
 /~仓库ID: AND WAREHOUSE.ID = {仓库ID}~/
GROUP BY LOC.ID,LOC.CODE  ORDER BY SUM((INV.QTY)) DESC

